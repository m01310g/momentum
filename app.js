// variable 사용
// const -> 값 변경 불가
const a = 5;
const b = 2;

// js는 camelCase 주로 사용
const myName = "m01310g";

console.log(a+b);
console.log(a/b);
console.log(a*b);

console.log("hello "+myName);

// let 사용해서 variable 정의
// let -> 값 변경 가능
let c = 5;
let d = 2;

console.log(c+d);
console.log(c/d);
console.log(c*d);